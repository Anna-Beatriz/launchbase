const idade = 17

// Verificar se a idade é maior ou igual a 18 anos
if (idade >= 18) {
    // Se sim, deixar entrar
    console.log('Entrada liberada.')
} else {
    // Se não, bloquear a entrada.
    console.log(`Idade: ${idade} anos. Entrada não autorizada.`)
}

// Se a pessoa tiver 17 anos
if (idade == 17) {
    // avisar para voltar quando fizer 18 anos
    console.log('Volte quando completar 18 anos!')
}
